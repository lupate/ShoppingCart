    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(jTable2.getRowCount() != 0){
            controller.saveOrder();
            JOptionPane.showMessageDialog(rootPane, "Confirmed successfully");
            controller.showCProfile();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Order is empty");
            controller.browseProducts();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated

        cart = controller.getList();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        for (Long cart1 : cart) {
            model.addRow(controller.getOneProductCart(cart1).toArray());
            
        }
        for(int i=0 ; i<cart.size() ; i++){
            payments += ((Double.parseDouble(String.valueOf(jTable2.getModel().getValueAt(i, 1))) - Double.parseDouble(String.valueOf(jTable2.getModel().getValueAt(i, 2)))*Double.parseDouble(String.valueOf(jTable2.getModel().getValueAt(i, 1)))/100.0)) * Long.parseLong(String.valueOf(controller.getQuantValues().get(i)));
            model.setValueAt(controller.getQuantValues().get(i), i, 3);
        }
        payment.setText(String.valueOf(payments)+"$");
    }//GEN-LAST:event_formWindowActivated

