    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //Add Button function
        try {

            //Getting data from TextField   
            String description = jTextField2.getText();

            if (description.equals(" ")) {
                JOptionPane.showMessageDialog(rootPane, "Enter Valid Data!");
            } else {
                //Inserting data to database       
                controller.addCategory(description);
                //Viewing data in jTable
                catTable.setModel(DbUtils.resultSetToTableModel(controller.viewCategory()));
                jTextField2.setText(" ");
            }

        } catch (SQLException ex) {
            System.out.println(ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

