    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Update Button Function
        try {
            //Getting selected row
            int i = catTable.getSelectedRow();
            if (i > 0) {
            //Getting data
                int catID = Integer.parseInt(catTable.getValueAt(i, 0).toString());
                String description = jTextField2.getText();
                catTable.setValueAt(jTextField2.getText(), i, 1);
             //Update Method
                controller.updateCategory(catID, description);
                JOptionPane.showMessageDialog(rootPane, "Updated!");
                jTextField2.setText(" ");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Select a row!");
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         //Delete Button Function
        try {
            int i = catTable.getSelectedRow();
            if (i > 0) {
           //Getting data
                int catID = Integer.parseInt(catTable.getValueAt(i, 0).toString());
           //Delete Method
                controller.deleteCategory(catID);
                JOptionPane.showMessageDialog(rootPane, "Deleted!");
               jTextField2.setText(" ");
            } else {
                JOptionPane.showMessageDialog(rootPane, " Select row!");
            }
        } catch (SQLException ex) {
           System.out.println(ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

